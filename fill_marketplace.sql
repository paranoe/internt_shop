-- =========================
-- СВЯЗЬ КАТЕГОРИЯ -> ПАРАМЕТР
-- =========================
INSERT INTO category_parameters (category_id, parameter_id)
SELECT c.category_id, p.parameter_id
FROM categories c
JOIN parameters p ON (
  (c.category_name = 'Электроника' AND p.name IN ('Бренд', 'Модель', 'Цвет', 'Диагональ', 'Память', 'Вес', 'Гарантия'))
  OR
  (c.category_name = 'Одежда и обувь' AND p.name IN ('Бренд', 'Цвет', 'Материал', 'Размер', 'Пол', 'Сезон'))
  OR
  (c.category_name = 'Красота и уход' AND p.name IN ('Бренд', 'Цвет', 'Объём', 'Вес'))
  OR
  (c.category_name = 'Дом и интерьер' AND p.name IN ('Бренд', 'Цвет', 'Материал', 'Размер', 'Вес'))
  OR
  (c.category_name = 'Детские товары' AND p.name IN ('Бренд', 'Цвет', 'Материал', 'Размер', 'Возраст', 'Вес'))
  OR
  (c.category_name = 'Спорт и отдых' AND p.name IN ('Бренд', 'Цвет', 'Материал', 'Размер', 'Вес'))
  OR
  (c.category_name = 'Авто' AND p.name IN ('Бренд', 'Модель', 'Совместимость', 'Артикул', 'Вес'))
);